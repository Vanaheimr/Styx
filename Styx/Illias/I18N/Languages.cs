        public static Languages Parse(String Text)
        {

            if (TryParse(Text, out var language))
                return language;

            throw new ArgumentException($"Unknown language '{Text}'!", nameof(Text));

        }

        public static Languages? TryParse(String Text)
        {

            if (TryParse(Text, out var language))
                return language;

            return null;

        }

        public static Boolean TryParse(String Text, out Languages Language)

            => Enum.TryParse(Text,
                             true,
                             out Language);

        public static String AsText(this Languages Language)
        {
            switch (Language)
            {

                default:
                    return Language.ToString();

            }
        }

        public static Languages3 ToLanguages3(this Languages Language)
        {
            switch (Language)
            {

                case Languages.de:
                    return Languages3.deu;

                default:
                    throw new ArgumentException("Unknow language!", nameof(Language));

            }
        }

        public static Languages ToLanguages(this Country Country)
        {
            switch (Country.Alpha2Code.ToLower())
            {

                case "au":
                case "de":
                    return Languages.de;
                //case "dk":
